def nettoyer_pour_geocodage(lieu):
    """
    Nettoie un lieu spécifiquement pour le géocodage :
    - Enlève les parenthèses et leur contenu
    - Enlève les préfixes comme "Sortie Métro"
    - Gère les cas spéciaux parisiens
    """
    if pd.isna(lieu) or lieu == '':
        return lieu

    lieu_original = str(lieu)
    lieu_lower = lieu_original.lower()

    # 🔴 CAS SPÉCIAUX PARISIENS (à traiter en priorité)
    cas_speciaux = {
        'bois de vincennes lac daumesnil': 'Lac Daumesnil, Paris',
        'lac daumesnil': 'Lac Daumesnil, Paris',
        'bois de vincennes': 'Bois de Vincennes, Paris',
        'canal de l ourcq parc de la villette': 'Canal de l\'Ourcq, Paris',
        'parc de la villette': 'Parc de la Villette, Paris',
        'quai de la tournelle devant notre dame': 'Quai de la Tournelle, Paris',
        'sortie metro barbes rochechouart': 'Barbès-Rochechouart, Paris',
        'barbes rochechouart': 'Barbès-Rochechouart, Paris',
    }

    for key, value in cas_speciaux.items():
        if key in lieu_lower:
            return value

    # Enlever le contenu des parenthèses
    lieu_sans_parentheses = re.sub(r'\([^)]*\)', '', lieu_original).strip()

    # Enlever les préfixes gênants
    prefixes = ['Sortie Métro', 'Sortie metro', 'Devant', 'Proche', 'Angle', 'Rue de la', 'Place de la']
    for prefix in prefixes:
        lieu_sans_parentheses = re.sub(prefix, '', lieu_sans_parentheses, flags=re.IGNORECASE).strip()

    # Nettoyer les espaces multiples
    lieu_propre = re.sub(r'\s+', ' ', lieu_sans_parentheses).strip()

    return lieu_propre

def generer_tentatives_geocodage(lieu, ville="Paris", pays="France"):
    """
    Génère une liste intelligente de tentatives pour le géocodage.
    """
    lieu_propre = nettoyer_pour_geocodage(lieu)

    tentatives = [
        # Formats standards
        f"{lieu_propre}, {ville}, {pays}",
        f"{lieu_propre}, {ville}",
        lieu_propre,
    ]

    # 🔴 Tentatives spécifiques basées sur des mots-clés
    lieu_lower = lieu_propre.lower()

    if 'daumesnil' in lieu_lower:
        tentatives.append("Lac Daumesnil, Paris")
        tentatives.append("Bois de Vincennes, Paris")

    if 'villette' in lieu_lower:
        tentatives.append("Parc de la Villette, Paris")
        tentatives.append("Canal de l'Ourcq, Paris")

    if 'barbes' in lieu_lower or 'barbès' in lieu_lower:
        tentatives.append("Barbès-Rochechouart, Paris")
        tentatives.append("Station Barbès, Paris")

    if 'tournelle' in lieu_lower:
        tentatives.append("Quai de la Tournelle, Paris")
        tentatives.append("Notre-Dame, Paris")

    # Supprimer les doublons
    tentatives_uniques = []
    for t in tentatives:
        if t not in tentatives_uniques:
            tentatives_uniques.append(t)

    return tentatives_uniques

