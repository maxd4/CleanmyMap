# Nettoyage des préfixes inutiles (mais avec exceptions)
def nettoyer_prefixes_lieu(lieu):
    """
    Nettoie les préfixes inutiles dans les noms de lieux
    Exemple: "Sortie Métro Barbès" -> "Sortie Métro Barbès" (conservé !)
    """
    if pd.isna(lieu) or lieu == '':
        return lieu

    lieu_original = str(lieu)
    lieu_lower = lieu_original.lower()

    # 🔴 EXCEPTIONS : NE PAS NETTOYER CES CAS
    exceptions = [
        'sortie metro', 'sortie métro', 'station metro', 'station métro',
        'entree metro', 'entrée métro', 'bouche metro', 'bouche métro'
    ]

    for exception in exceptions:
        if exception in lieu_lower:
            return lieu_original  # Garder le nom original

    # Pour les autres cas, nettoyer les préfixes
    pattern = '|'.join(prefixes_a_supprimer)
    lieu_nettoye = re.sub(pattern, '', lieu_original, flags=re.IGNORECASE).strip()
    lieu_nettoye = re.sub(r'\s+', ' ', lieu_nettoye)

    return lieu_nettoye

def normaliser_nom_lieu(lieu):
    """
    Normalise un nom de lieu pour le rendre plus robuste aux variations
    MAIS en préservant les noms de stations/services
    """
    if pd.isna(lieu) or lieu == '' or lieu == 'nan' or lieu == 'None':
        return ''

    # Convertir en string
    lieu = str(lieu)

    # 🔴 Vérifier si c'est une station/service à préserver
    lieu_lower = lieu.lower()
    mots_a_preserver = ['metro', 'métro', 'station', 'gare', 'aeroport', 'aéroport']

    if any(mot in lieu_lower for mot in mots_a_preserver):
        # Pour les stations, on normalise moins agressivement
        lieu = unidecode.unidecode(lieu)
        lieu = re.sub(r'[-_\'’`"()]', ' ', lieu)
        lieu = re.sub(r'[.,;:!?]', '', lieu)
        lieu = re.sub(r'\s+', ' ', lieu).strip()
        return lieu

    # Normalisation standard pour les autres lieux
    lieu = unidecode.unidecode(lieu)
    lieu = lieu.lower()
    lieu = re.sub(r'[-_\'’`"()]', ' ', lieu)
    lieu = re.sub(r'[.,;:!?]', '', lieu)
    lieu = re.sub(r'\s+', ' ', lieu).strip()

    return lieu

def nettoyer_pour_comparaison(lieu):
    """
    Nettoie un lieu pour la comparaison (version plus douce)
    """
    if pd.isna(lieu) or lieu == '':
        return ''

    lieu = normaliser_nom_lieu(lieu)

    # 🔴 Mots génériques à supprimer (version réduite)
    mots_generiques = [
        'la', 'le', 'les', 'du', 'de', 'des', 'a', 'au', 'aux', 'et', 'sur'
    ]

    mots = lieu.split()
    mots_filtres = [m for m in mots if m not in mots_generiques]

    # Si on a trop filtré, on garde les mots originaux
    if len(mots_filtres) < 2:
        return lieu

    return ' '.join(mots_filtres)

def detecter_type_coordonnees(lieu):
    """Détecte si le lieu est une adresse texte ou des coordonnées GPS"""
    if pd.isna(lieu) or lieu == '':
        return 'inconnu'

    lieu_str = str(lieu).strip()

    # Pattern pour coordonnées décimales
    pattern_decimal = r'^(-?\d+\.?\d*)\s*[,;]\s*(-?\d+\.?\d*)$'
    if re.match(pattern_decimal, lieu_str):
        return 'coords_decimal'

    # Pattern pour coordonnées DMS
    pattern_dms = r'\d+[°\s]+\d+[\'\s]+\d+(?:\.\d+)?["\s]*[NS]\s*\d+[°\s]+\d+[\'\s]+\d+(?:\.\d+)?["\s]*[EW]'
    if re.search(pattern_dms, lieu_str, re.IGNORECASE):
        return 'coords_dms'

    return 'adresse'

def clean_name(s):
    """Nettoie un nom pour l'utiliser comme clé de cache."""
    if pd.isna(s) or s == '':
        return ''
    if not isinstance(s, str):
        s = str(s)
    return re.sub(r'[^a-z0-9]', '', s.lower())

