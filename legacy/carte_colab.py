def geocoder_lieu(lieu_original, lieu_normalise, type_saisie, cache, force_recache=False):
    """
    Fonction principale de géocodage qui utilise les données normalisées.
    Version améliorée avec plus d'intelligence.
    """
    if pd.isna(lieu_original) or lieu_original == '':
        return None, None, "vide"

    l_clean = re.sub(r'[^a-z0-9]', '', lieu_normalise.lower())

    if not force_recache and l_clean in cache:
        return cache[l_clean]['lat'], cache[l_clean]['lon'], f"cache_{cache[l_clean]['methode']}"

    if type_saisie == 'coords_decimal':
        res = extraire_decimal_direct(lieu_original)
        if res:
            return res[0], res[1], "decimal_direct"

    if type_saisie == 'coords_dms':
        res = convertir_dms_en_decimal(lieu_original)
        if res:
            return res[0], res[1], "dms"

    if type_saisie == 'adresse':
        # Essayer d'abord OSMnx (plus rapide)
        res = geocoder_avec_osmnx(lieu_normalise)
        if res:
            return res[0], res[1], "osmnx"

        # Puis Nominatim (plus complet mais plus lent)
        res = geocoder_avec_nominatim(lieu_normalise)
        if res:
            return res[0], res[1], "nominatim"

        # 🔴 Dernière tentative : version ultra-simplifiée
        lieu_simplifie = nettoyer_pour_geocodage(lieu_normalise)
        if lieu_simplifie != lieu_normalise:
            res = geocoder_avec_nominatim(lieu_simplifie)
            if res:
                return res[0], res[1], "nominatim_simplifie"

    return None, None, "echec"

