def formater_lieu_google_maps(row):
    """
    Formate un lieu pour qu'il ressemble à Google Maps
    Gère les adresses texte et les coordonnées GPS
    """

    # Récupérer le lieu original
    lieu_original = None
    for col in ['lieu_original', 'lieu_brut', 'lieu_complet', 'adresse']:
        if col in row.index and pd.notna(row[col]) and str(row[col]) != 'nan':
            lieu_original = str(row[col]).strip()
            break

    # Si c'est des coordonnées GPS
    if lieu_original and re.match(r'^[-+]?\d*\.?\d+', lieu_original):
        try:
            from geopy.geocoders import Nominatim
            geolocator = Nominatim(user_agent="brigades_elites")
            location = geolocator.reverse(f"{row['lat']}, {row['lon']}")
            if location and location.address:
                adresse = location.address.split(',')
                return f"📍 {','.join(adresse[:2]).strip()}"
        except:
            pass
        return f"📍 {row['lat']:.4f}, {row['lon']:.4f}"

    # Si on a un lieu original
    if lieu_original and lieu_original.lower() != 'lieu inconnu':
        lieu = re.sub(r'[_\-]', ' ', lieu_original)
        mots = lieu.split()
        mots_formates = []

        mots_minuscules = ['de', 'du', 'des', 'le', 'la', 'les', 'l', 'd',
                          'a', 'au', 'aux', 'sur', 'sous', 'et', 'en']

        for i, mot in enumerate(mots):
            mot = mot.strip()
            if mot.lower() in mots_minuscules and i != 0:
                mots_formates.append(mot.lower())
            else:
                if mot and len(mot) > 0:
                    mots_formates.append(mot[0].upper() + mot[1:].lower() if len(mot) > 1 else mot.upper())

        lieu_formate = ' '.join(mots_formates)

        corrections = {
            'St ': 'Saint-',
            'Ste ': 'Sainte-',
            'Notre Dame': 'Notre-Dame',
        }
        for ancien, nouveau in corrections.items():
            lieu_formate = lieu_formate.replace(ancien, nouveau)

        return f"📍 {lieu_formate}"

    return "📍 Lieu non spécifié"

