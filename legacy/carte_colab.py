def geocoder_lieu(lieu):
    """Convertit une adresse/coordonnées en lat/lon"""
    if pd.isna(lieu) or lieu == '' or lieu == 'nan':
        return None, None

    lieu = str(lieu).strip()

    # Essayer format décimal
    try:
        match = re.search(r'(-?\d+\.?\d*)\s*[,;\s]+\s*(-?\d+\.?\d*)', lieu)
        if match:
            lat, lon = float(match.group(1)), float(match.group(2))
            if -90 <= lat <= 90 and -180 <= lon <= 180:
                return lat, lon
    except:
        pass

    # Essayer format DMS
    try:
        pattern = r'(\d+)[°\s]+(\d+)[\'\s]+(\d+(?:\.\d+)?)[\"\s]*([NS])\s*(\d+)[°\s]+(\d+)[\'\s]+(\d+(?:\.\d+)?)[\"\s]*([EW])'
        match = re.search(pattern, lieu.upper())
        if match:
            lat = float(match.group(1)) + float(match.group(2))/60 + float(match.group(3))/3600
            if match.group(4) == 'S': lat *= -1
            lon = float(match.group(5)) + float(match.group(6))/60 + float(match.group(7))/3600
            if match.group(8) == 'W': lon *= -1
            return lat, lon
    except:
        pass

    return None, None

