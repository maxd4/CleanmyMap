import unicodedata

def detecter_type_lieu(lieu):
    """Détecte le type de lieu à partir du nom"""
    if pd.isna(lieu):
        return 'autre'

    lieu_lower = str(lieu).lower()
    lieu_norm = ''.join(
        c for c in unicodedata.normalize('NFD', lieu_lower)
        if unicodedata.category(c) != 'Mn'
    )

    # Espaces verts (avec mots-clés élargis)
    if any(kw in lieu_norm for kw in [
        'jardin', 'parc', 'square', 'esplanade', 'bois',
        'pelouse', 'forest', 'woods', 'garden', 'park',
        'vert', 'luxembourg', 'tuileries', 'butte',
        'montmartre', 'plantes', 'lucernaire', 'plante',
        'botanique', 'floral', 'arboretum', 'potager'
    ]):
        return 'espace_vert'

    # Monuments et sites remarquables
    if any(mot in lieu_norm for mot in [
        'monument', 'tour eiffel', 'eiffel', 'statue', 'obelisque',
        'cathedrale', 'basilique', 'arc de triomphe', 'pantheon', 'invalides'
    ]):
        return 'monument'

    # Rues et voies
