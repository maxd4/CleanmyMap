print(f"\n📈 Statistiques {annee_courante} :")
print(f"  • Actions : {nb_actions}")
print(f"  • Bénévoles : {total_benevoles:.0f}")
print(f"  • Mégots : {total_megots:,.0f}".replace(',', ' '))
print(f"  • Déchets : {total_dechets:.1f} kg")
print(f"\n🌍 Impact environnemental :")
print(f"  • CO₂ évité : {co2_total:.1f} kg (🚗 {km_voiture:.0f} km)")
print(f"  • Eau préservée : {eau_totale:.0f} L (🚿 {nb_douches:.0f})")

