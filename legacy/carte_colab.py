def preparer_donnees(df_raw):
    """Nettoie et prépare les données avec géocodage"""

    # Mapping des colonnes (à adapter selon vos noms exacts)
    mapping = {
        'adresse precise ou coordo GPS': 'adresse',
        'ville': 'ville',
        'type': 'type_lieu',
        'date': 'date',
        'nbr megots': 'megots',
        'kg dechets': 'dechets_kg',
        "nom d'association": 'association',
        'temps en min': 'temps_min',
        'nombre benevoles': 'nb_benevoles'
    }

    # Renommer les colonnes existantes
    df = df_raw.copy()
    for old, new in mapping.items():
        if old in df.columns:
            df = df.rename(columns={old: new})

    # Créer les colonnes standardisées
    result = pd.DataFrame()

    # Date
    if 'date' in df.columns:
        result['date'] = pd.to_datetime(df['date'], errors='coerce', dayfirst=True)
    else:
        result['date'] = pd.NaT

    # Adresse et géocodage
    if 'adresse' in df.columns:
        result['adresse'] = df['adresse'].astype(str)
        print("🗺️ Géocodage en cours...")
        coords = result['adresse'].apply(geocoder_lieu)
        result['lat'] = [c[0] for c in coords]
        result['lon'] = [c[1] for c in coords]
    else:
        result['adresse'] = ''
        result['lat'] = None
        result['lon'] = None

    # Autres colonnes
    result['ville'] = df.get('ville', 'Paris').astype(str)
    result['type_lieu'] = df.get('type_lieu', 'Non spécifié').astype(str)
    result['association'] = df.get('association', 'Inconnue').astype(str)

    # Données numériques
    result['megots'] = pd.to_numeric(df.get('megots', 0), errors='coerce').fillna(0)
    result['dechets_kg'] = pd.to_numeric(df.get('dechets_kg', 0), errors='coerce').fillna(0)
    result['temps_min'] = pd.to_numeric(df.get('temps_min', 1), errors='coerce').fillna(1).replace(0, 1)
    result['nb_benevoles'] = pd.to_numeric(df.get('nb_benevoles', 1), errors='coerce').fillna(1).replace(0, 1)

    # Métriques calculées
    result['megots_par_heure_ben'] = result['megots'] / ((result['temps_min']/60) * result['nb_benevoles'])
    result['kg_par_heure_ben'] = result['dechets_kg'] / ((result['temps_min']/60) * result['nb_benevoles'])

    # Nettoyage
    result = result.dropna(subset=['date']).sort_values('date').reset_index(drop=True)

    print(f"✅ {len(result)} lignes préparées")
    print(f"🗺️ {result['lat'].notna().sum()} points géocodés")

    return result

