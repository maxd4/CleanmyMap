def t(key: str) -> str:
    """Helper to get translated text based on st.session_state.lang."""
    lang = st.session_state.get("lang", "fr")
    val = TRANSLATIONS.get(lang, TRANSLATIONS["fr"]).get(key, key)
    return repair_mojibake_text(val)

def i18n_text(fr: str, en: str) -> str:
    """Returns FR or EN text based on current language."""
    lang = st.session_state.get("lang", "fr")
    return repair_mojibake_text(fr if lang == "fr" else en)
